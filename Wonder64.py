import tkinter as tk

# Create the main window
root = tk.Tk()

# Set window title
root.title("Wonder 64 Build 4.22.24 0.1$")

# Set fixed window size
root.geometry("600x400")

# Disable resizing the window
root.resizable(False, False)

# Start the GUI event loop
root.mainloop()
